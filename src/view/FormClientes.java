    public void salvar(){
        if(!txt_nome.getText().isEmpty()|| !txt_telefone.getText().isEmpty()
                || !txt_endereco.getText().isEmpty()){
             cliente = new Cliente();
             cliente.setNome(txt_nome.getText());
             cliente.setTelefone(txt_telefone.getText());
             cliente.setEndereco(txt_endereco.getText());
             cliente.setNumero(Integer.parseInt(txt_numero.getText()));
             cliente.setComplemento(txt_complemento.getText());
             cliente.setBairro(String.valueOf(cb_bairros.getSelectedItem()));
             cDAO.SalvarClientes(cliente);
             
             JOptionPane.showMessageDialog(this, "Cliente Salvo com Sucesso!");
             listarTabela();
             limparComponentes();
        }else{
            JOptionPane.showMessageDialog(this, "Preenche os campos Descrição e Preço");
        }
        bt_novo.setEnabled(true);
        bt_salvar.setEnabled(false);
    }
     public final void listarTabela(){
        String sql = "SELECT * FROM tb_clientes";
        ArrayList dados = new ArrayList();
        
        String[] Colunas = new String[]{"Código","Nome","Telefone","Endereço"};
        conexao.conecta();
        conexao.executaSQL(sql);
        
               
        try {
            conexao.rs.first();
            do{
                dados.add(new Object[]{
                                                       conexao.rs.getInt("codigo_cliente"),
                                                       conexao.rs.getString("nome"),
                                                       conexao.rs.getString("telefone"),
                                                       conexao.rs.getString("endereco")
                                                               +" nº "+
                                                       conexao.rs.getString("numero")
                                                               +" - "+
                                                       conexao.rs.getString("bairro"),
                                                       });
            
            }while(conexao.rs.next());
        } catch (SQLException ex) {
            System.out.println("Erro ao Preencher a Tabela"+ ex);
        }
         ModeloTabela modelo = new ModeloTabela(dados, Colunas);
         tabela.setModel(modelo);
         tabela.getColumnModel().getColumn(0).setPreferredWidth(80);
         tabela.getColumnModel().getColumn(0).setResizable(false);
         tabela.getColumnModel().getColumn(1).setPreferredWidth(300);
         tabela.getColumnModel().getColumn(1).setResizable(false);
         tabela.getColumnModel().getColumn(2).setPreferredWidth(100);
         tabela.getColumnModel().getColumn(2).setResizable(false);
         tabela.getColumnModel().getColumn(3).setPreferredWidth(400);
         tabela.getColumnModel().getColumn(3).setResizable(false);
                             
         tabela.getTableHeader().setReorderingAllowed(false);
         tabela.setAutoResizeMode( tabela.AUTO_RESIZE_OFF);
         tabela.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
     
       
     }
      public ResultSet listarBairros(){
        
        cb_bairros.removeAllItems();
        
        conexao.conecta();
        conexao.executaSQL("select * from tb_bairros order by nome_bairro");
        
        ResultSet rs=null;//representação na memoria de um tabela
       
        
        try{
            conexao.rs.first();
           
            do{
                cb_bairros.addItem(conexao.rs.getString("nome_bairro"));
            }
            while (conexao.rs.next());{
              conexao.Desconecta();
            }
      
        }catch(Exception e){
            System.out.println(e.getMessage());
            conexao.Desconecta();
                
        }
        return rs;
    }
