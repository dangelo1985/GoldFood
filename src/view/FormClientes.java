    public Boolean testarBairro(String bairro){
        boolean teste = true;
        conexao.conecta();
        conexao.executaSQL("SELECT * FROM tb_bairros where nome_bairro = '"+bairro+"'");
        try {
            if(!conexao.rs.first()){
                teste = false;
            }
                 
        } catch (SQLException ex) {
            Logger.getLogger(FormClientes.class.getName()).log(Level.SEVERE, null, ex);
          
        }
        conexao.Desconecta();
        return teste;
    }
