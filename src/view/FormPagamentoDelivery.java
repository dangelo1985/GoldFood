    public void carregarComponentes(){
        tipo = (String)cb_tipo.getSelectedItem();
        totalProdutos = Double.parseDouble(lbl_totalProdutos.getText().replaceAll(",", "."));
        if(!txt_entrega.getText().isEmpty()){
            taxaEntrega = Double.parseDouble(txt_entrega.getText().replaceAll(",", "."));
            if(!txt_dinheiro.getText().isEmpty()){
                 dinheiro = Double.parseDouble(txt_dinheiro.getText().replaceAll(",", "."));
                 if(!txt_cartão.getText().isEmpty()){
                     cartao = Double.parseDouble(txt_cartão.getText().replaceAll(",", "."));
                    
                 }else{
                     cartao = 0.00;
                 }
            }else{
                dinheiro = 0.00;
            }
        }else{
            taxaEntrega = 0.00;
        }
        
        total = totalProdutos+taxaEntrega;
        lbl_total.setText(decimal.format(total));
        
       
    }

