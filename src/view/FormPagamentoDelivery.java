    public void calcularTroco(){
                       
            troco = dinheiro - Double.parseDouble(lbl_total.getText().replaceAll(",", "."));
            
            troco += cartao;
            
            lbl_troco.setText(decimal.format(troco));
            if(troco < 0){
                lbl_falta.setText("FALTA R$ ");
                lbl_falta.setForeground(Color.RED);
            }else{
                lbl_falta.setText("TROCO RS ");
                lbl_falta.setForeground(Color.BLUE);
            }
             
            
    }
    
 

