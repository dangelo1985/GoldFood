    public void salvar(){
        carregarDelivery();
        deliveryDAO.cadastraVendaDelivery(delivery);
        JOptionPane.showMessageDialog(this, "Pedido Cadastrado!");
        frame.dispose();
        this.dispose();
    }
    public void alterar(){
        carregarDelivery();
        delivery.setStatus("FECHADO");
        deliveryDAO.alterarDelivery(delivery);
        
        JOptionPane.showMessageDialog(this, "Pedido Alterado!");
        this.dispose();
    }
    public void carregarDelivery(){
        delivery.setEntrega(Float.parseFloat(txt_entrega.getText().replaceAll(",", ".")));
        delivery.setTotalPedido(Float.parseFloat(lbl_valorTotal.getText().replaceAll(",", ".")));
        delivery.setTroco(Float.parseFloat(lbl_troco.getText().replaceAll(",", ".")));
        delivery.setCartao(Float.parseFloat(txt_cartão.getText().replace(",", ".")));
        delivery.setDinheiro(Float.parseFloat(txt_dinheiro.getText().replaceAll(",", ".")));
        delivery.setTipo((String)cb_tipo.getSelectedItem());
    }
    private void cb_tipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cb_tipoActionPerformed
        carregarComponentes();
        if(!tipo.equals("Entrega")){
            double entrega = Double.parseDouble(txt_entrega.getText().replaceAll(",", "."));
            double dinheiro = Double.parseDouble(txt_dinheiro.getText().replaceAll(",", "."));
            txt_dinheiro.setText(decimal.format(dinheiro - entrega));
            txt_entrega.setText("0,00");
            txt_entrega.setEnabled(false);
            calcular();
            
           
        }else{
            txt_entrega.setEnabled(true);
        }
  
    }//GEN-LAST:event_cb_tipoActionPerformed

